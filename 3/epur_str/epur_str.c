#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (ac == 2)
	{
		while (((av[1][i] >= 9 && av[1][i] <= 13) || av[1][i] == 32) && av[1][i])
			i++;
		while (av[1][i])
		{
			while (av[1][i] && av[1][i] != 9 && av[1][i] != 10 && av[1][i] != 11 && av[1][i] != 12 && av[1][i] != 13 && av[1][i] != 32)
			{
				av[1][j] = av[1][i];
				i++;
				j++;
			}
			while ((av[1][i] >= 9 && av[1][i] <= 13) || av[1][i] == 32)
				i++;
			if (av[1][i])
			{
				av[1][j] = 32;
				j++;
			}
			else
				av[1][j] = '\0';
		}
		write(1, av[1], j);
	}
	write(1, "\n", 1);
	return (0);
}
