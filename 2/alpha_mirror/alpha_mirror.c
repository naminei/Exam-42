#include <unistd.h>

int main(int ac, char **av)
{
	int j;

	j = 0;
	if (ac == 2)
	{
		while(av[1][j])
		{
			if (av[1][j] >= 65 && av[1][j] <= 90)
				av[1][j] = 90 - av[1][j] + 65;
			if (av[1][j] >= 97 && av[1][j] <= 122)
				av[1][j] = 122 - av[1][j] + 97;
			j++;
		}
		write(1, av[1], j);
	}
	write(1, "\n", 1);
}
